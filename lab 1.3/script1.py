# demonstrate the use of type casting constructor 
# Take input from the user as a string 
# Print the converted values along their types 
a = input("Enter the number: ") 
b = int(a)  
c = float(a) 
d = bool(a)   
print(f"B = {b}") 
print(f"Type of B = {type(b)}") 
print(f"C = {c}")
print(f"Type of C = {type(c)}") 
print(f"D = {d}") 
print(f"Type of D = {type(d)}") 
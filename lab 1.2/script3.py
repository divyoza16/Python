# To perform add, sub, mul, div, floor div, mod and exponentiation 
a = int(input("Enter the first number: ")) 
b = int(input("Enter the second number: ")) 
sum = a+b
sub = a-b
mul = a*b 
div = a/b
div1 = a//b 
mod = a%b
exp = a**b 
print(f"Sum = {sum}")
print(f"Sub = {sub}") 
print(f"Mul = {mul}") 
print(f"Div = {div}") 
print(f"Floor div = {div1}") 
print(f"Mod = {mod}") 
print(f"Exponentiation = {exp}") 